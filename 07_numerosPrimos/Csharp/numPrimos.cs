using System;
public class numPrimos
{
    public static void Main(String[] args)
    {
        int n,i;
        bool comp=false;
        Console.Write("Ingresa un numero: ");
        n=Convert.ToInt32(Console.ReadLine());
        for(i=2;i<n;i++)
        {
            if(n%i==0)
            {
                comp=true;
                Console.Write("No es primo\n");
                break;
            }
        }
        if(!comp)
            Console.Write("Es primo\n");
    }
}